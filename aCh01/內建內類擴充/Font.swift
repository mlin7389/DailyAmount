import Foundation
import SwiftUI

extension Font {
    
    public static func cusTextField() -> Font {
       return Font.system(size: 16)
    }
    
    
    public static func cusListTitle() -> Font {
       return Font.system(size: 18)
    }
    
    public static func cusSubTotal() -> Font {
       return Font.system(size: 16)
    }
    
    public static func cusListSubTitle() -> Font {
       return Font.system(size: 14)
    }

}
